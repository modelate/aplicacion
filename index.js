function recomendarFaja() {
    const objetivo = document.getElementById('objetivo').value;
    const area = document.getElementById('area').value;
    const material = document.getElementById('material').value;
    const compresion = document.getElementById('compresion').value;

    if (!objetivo || !area || !compresion) {
        alert('Por favor, completa todos los campos.');
        return;
    }

    $('.loading').show();
    $('#result').hide();

    setTimeout(() => {
        let fajasRecomendadas = encontrarFajasIdeales(objetivo, area, material, compresion);

        if (fajasRecomendadas.length > 0) {
            mostrarRecomendaciones(fajasRecomendadas);
        } else {
            document.getElementById('result').innerHTML = '<p>Lo sentimos, no encontramos fajas que se ajusten exactamente a tus criterios. Te recomendamos explorar nuestra colección completa o ajustar tus preferencias.</p>';
        }

        $('.loading').hide();
        $('#result').fadeIn(500);
    }, 2000);
}

function encontrarFajasIdeales(objetivo, area, material, compresion) {
    return fajas.filter(faja => {
        return faja.objetivo.includes(objetivo) &&
               faja.area.includes(area) &&
               (material === 'sin_preferencia' || faja.material.includes(material)) &&
               faja.compresion === compresion;
    });
}

